  <h2 class="article--title">
    Présentation personnelle :
  </h2>
  <p class="article--text">
        Depuis 2013, j'ai le bonheur d'accompagner les autres à travers le mouvement pour les aider à se sentir mieux, tant physiquement que mentalement. Dès le début, j'ai été captivé par l'impact positif que l'exercice peut avoir sur le bien-être général, la prise de confiance et la diminution du stress. 
</p>
<p class="article--text">
Cependant, j'ai également rencontré de nombreuses personnes dont le potentiel était limité en raison de douleurs, de fatigue, d'état émotionnel préoccupant, et surtout de contraintes biomécaniques. </br>
</p>
<p class="article--text">
Passant des années à comprendre ces déséquilibres musculaires et les douleurs qui les accompagnent, j’ai exploré et appliqué plein de techniques pour répondre à ces problématiques, mais les résultats n'étaient souvent pas durables. </br>
</p>
<p class="article--text">
En 2019, j’ai découvert que l’approche du système nerveux et la posturologie était la clé pour obtenir des explications et des résultats durables. En effet, le cerveau est à la base de toutes les contractions musculaires, et c'est lui qui est responsable de nos tensions et de nos douleurs. Ces réactions sont en réalité des mécanismes de protection, car le but ultime du cerveau est de nous maintenir en vie.
</p>
<p class="article--text">

Aujourd’hui, je vais donc continuer à faire bouger des personnes, mais pas n’importe comment. Je veux que cela se fasse sans douleur et en explorant leur plein potentiel.</br>
</p>
<p class="article--text">

Un public qui me tient particulièrement à cœur, ce sont les enfants. Ils sont les plus touchés par les évolutions rapides de notre environnement. De plus en plus d'entre eux sont étiquetés comme hyperactifs ou présentent des troubles "dys".  Naturellement, on cherche à remédier à ces problèmes par le biais de l'orthophonie, mais on oublie que le mouvement est fondamental pour le développement de l'être humain et pour l'accès à son potentiel cognitif. Le lien entre le mouvement, la posture et la cognition reste largement méconnu. Les orthophonistes sont débordés, alors qu'il y a tant à faire par le mouvement pour aider ces enfants. 
</p>
<p class="article--text">

Voilà pourquoi je suis là aujourd'hui à vous proposer mes services. La posturologie n'est pas une solution miracle, mais elle constitue un outil indispensable et trop souvent négligé dans notre société moderne. Je suis convaincue que chacun peut bénéficier de son approche holistique pour améliorer sa qualité de vie. N'hésitez pas à faire appel à mes services pour découvrir comment la posturologie peut vous aider à atteindre vos objectifs de bien-être.
</p>
