    <h2>
  Hernie discale :
    </h2>
    <p>
    Que ce soit pour prévenir une hernie discale ou pour la traiter, la posturologie est une approche idéale. Ne blâmez pas les 'faux mouvements' ou 'trop de mouvements'. Ce n'est pas cela qui a endommagé votre disque. Cela résulte principalement d'une longue période de compensation : un bassin incliné et/ou en rotation crée des contraintes significatives, souvent accumulées sur de nombreuses années. La blessure survient souvent comme la dernière goutte qui fait déborder le vase.
    </p>
